package FundamentalsExersisesAdvanced;

public class Calculator {
    double x;
    double y;

    public Calculator() {
        this.x = x;
        this.y = y;
    }

    public double getSum(double x, double y){
        return x+y;
    }

    public double getMulti(double x, double y){
        return x*y;
    }

    public double getSub(double x, double y){
        return x - y;
    }

}
